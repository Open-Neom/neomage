// ---------------------------------------------------------------------------
// Message origin
// ---------------------------------------------------------------------------

/// Provenance of a queued command or user message.
///
/// Tracks where a message originated so the transcript can record origin
/// structurally rather than relying on XML tags in content.
enum MessageOrigin {
  /// Typed by the user at the keyboard.
  human,

  /// Received from a remote bridge (mobile / web client).
  bridge,

  /// Generated by a cron / scheduled task.
  cron,

  /// System-generated (proactive tick, resource update, etc.).
  system,
}

// ---------------------------------------------------------------------------
// Inline ghost text
// ---------------------------------------------------------------------------

/// Inline ghost text shown during mid-input command autocomplete.
class InlineGhostText {
  /// The ghost text to display (e.g. "mit" for /commit).
  final String text;

  /// The full command name (e.g. "commit").
  final String fullCommand;

  /// Position in the input where the ghost text should appear.
  final int insertPosition;

  /// Creates an [InlineGhostText].
  const InlineGhostText({
    required this.text,
    required this.fullCommand,
    required this.insertPosition,
  });
}

// ---------------------------------------------------------------------------
// Vim mode
// ---------------------------------------------------------------------------

/// Vim editor modes.
enum VimMode {
  /// Insert mode — keystrokes produce text.
  insert,

  /// Normal mode — keystrokes are interpreted as commands.
  normal;

  /// Display label matching the original TypeScript union values.
  String get label => switch (this) {
    VimMode.insert => 'INSERT',
    VimMode.normal => 'NORMAL',
  };
}

// ---------------------------------------------------------------------------
// Input state
// ---------------------------------------------------------------------------

/// Paste buffer state.
class PasteState {
  /// Accumulated paste chunks.
  final List<String> chunks;

  /// Creates a [PasteState].
  const PasteState({this.chunks = const []});
}

/// Common properties shared by all input state variants.
class BaseInputState {
  /// Rendered (possibly wrapped) value of the input.
  final String renderedValue;

  /// Character offset of the cursor within the full text.
  final int offset;

  /// Cursor line (0-indexed) within the rendered text, accounting for wrapping.
  final int cursorLine;

  /// Cursor column (display-width) within the current line.
  final int cursorColumn;

  /// Character offset where the viewport starts (0 when no windowing).
  final int viewportCharOffset;

  /// Character offset where the viewport ends (text.length when no windowing).
  final int viewportCharEnd;

  /// Whether a paste operation is in progress.
  final bool isPasting;

  /// Active paste buffer state, if any.
  final PasteState? pasteState;

  /// Creates a [BaseInputState].
  const BaseInputState({
    required this.renderedValue,
    required this.offset,
    required this.cursorLine,
    required this.cursorColumn,
    required this.viewportCharOffset,
    required this.viewportCharEnd,
    this.isPasting = false,
    this.pasteState,
  });
}

/// State for a standard (non-vim) text input.
typedef TextInputState = BaseInputState;

/// State for a vim-mode text input, extending [BaseInputState] with
/// the current [VimMode].
class VimInputState extends BaseInputState {
  /// The current vim mode.
  final VimMode mode;

  /// Creates a [VimInputState].
  const VimInputState({
    required super.renderedValue,
    required super.offset,
    required super.cursorLine,
    required super.cursorColumn,
    required super.viewportCharOffset,
    required super.viewportCharEnd,
    super.isPasting,
    super.pasteState,
    this.mode = VimMode.insert,
  });

  /// Returns a copy with the given [mode].
  VimInputState copyWithMode(VimMode mode) => VimInputState(
    renderedValue: renderedValue,
    offset: offset,
    cursorLine: cursorLine,
    cursorColumn: cursorColumn,
    viewportCharOffset: viewportCharOffset,
    viewportCharEnd: viewportCharEnd,
    isPasting: isPasting,
    pasteState: pasteState,
    mode: mode,
  );
}

// ---------------------------------------------------------------------------
// Prompt input mode
// ---------------------------------------------------------------------------

/// Input modes for the prompt.
enum PromptInputMode {
  /// Standard conversational prompt.
  prompt,

  /// Bash / shell command input.
  bash,

  /// An orphaned permission prompt waiting for resolution.
  orphanedPermission,

  /// A background-task notification.
  taskNotification;

  /// Whether this mode supports direct text editing.
  bool get isEditable => switch (this) {
    PromptInputMode.prompt || PromptInputMode.bash => true,
    _ => false,
  };
}

// ---------------------------------------------------------------------------
// Queue priority
// ---------------------------------------------------------------------------

/// Priority levels for queued commands.
///
/// - [now]   -- Interrupt and send immediately (aborts in-flight tool call).
/// - [next]  -- Send after the current tool call finishes, before the next
///              API round-trip.
/// - [later] -- Send after the current turn completes as a new query.
enum QueuePriority {
  /// Interrupt and send immediately.
  now,

  /// Send between tool-call result and next API round-trip.
  next,

  /// Wait for the current turn to finish, then process.
  later,
}

// ---------------------------------------------------------------------------
