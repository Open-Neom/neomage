  final PermissionResult permissionResult;

  /// The assistant message that triggered the permission request.
  final Message assistantMessage;

  /// Creates an [OrphanedPermission].
  const OrphanedPermission({
    required this.permissionResult,
    required this.assistantMessage,
  });
}

// ---------------------------------------------------------------------------
// Pasted content helpers
// ---------------------------------------------------------------------------

/// Represents pasted content (text or image) attached to a queued command.
class InputPastedContent {
  /// Unique identifier for this pasted item.
  final int id;

  /// Content type: `'text'` or `'image'`.
  final String type;

  /// The raw content string (plain text or base64-encoded image data).
  final String content;

  /// Creates an [InputPastedContent].
  const InputPastedContent({
    required this.id,
    required this.type,
    required this.content,
  });

  /// Whether this is a valid (non-empty) image paste.
  bool get isValidImage => type == 'image' && content.isNotEmpty;
}

// ---------------------------------------------------------------------------
// Queued command
// ---------------------------------------------------------------------------

/// A command queued for deferred execution when the model is busy.
class QueuedCommand {
  /// The prompt value — either a plain string or structured content blocks.
  final Object /* String | List<ContentBlock> */ value;

  /// The input mode under which this command was submitted.
  final PromptInputMode mode;

  /// Drain priority. Defaults to the priority implied by [mode] when
  /// enqueued.
  final QueuePriority? priority;

  /// Optional unique identifier for this command.
  final String? uuid;

  /// An orphaned permission still pending resolution.
  final OrphanedPermission? orphanedPermission;

  /// Raw pasted contents including images. Images are resized at
  /// execution time. Keyed by paste index.
  final Map<int, InputPastedContent>? pastedContents;

  /// The input string before `[Pasted text #N]` placeholders were
  /// expanded. Used for keyword detection so pasted content containing
  /// the keyword does not trigger unintended behaviour. Falls back to
  /// [value] when unset.
  final String? preExpansionValue;

  /// When `true`, the input is treated as plain text even if it starts
  /// with `/`. Used for remotely-received messages that should not
  /// trigger local slash commands.
  final bool skipSlashCommands;

  /// When `true`, slash commands are dispatched but filtered through a
  /// bridge-safety check. Unsafe commands return a helpful error instead
  /// of executing.
  final bool bridgeOrigin;

  /// When `true`, the resulting message gets `isMeta: true` -- hidden in
  /// the transcript UI but visible to the model.
  final bool isMeta;

  /// Provenance of this command (e.g. human keyboard, bridge, cron).
  final MessageOrigin? origin;

  /// Workload tag threaded through to billing attribution.
  final String? workload;

  /// Agent that should receive this notification. `null` = main thread.
  final AgentId? agentId;

  /// Creates a [QueuedCommand].
  const QueuedCommand({
    required this.value,
    required this.mode,
    this.priority,
    this.uuid,
    this.orphanedPermission,
    this.pastedContents,
    this.preExpansionValue,
    this.skipSlashCommands = false,
    this.bridgeOrigin = false,
    this.isMeta = false,
    this.origin,
    this.workload,
    this.agentId,
  });
}

// ---------------------------------------------------------------------------
// Utility functions
// ---------------------------------------------------------------------------

/// Returns `true` when [content] represents a non-empty image paste.
bool isValidImagePaste(InputPastedContent content) => content.isValidImage;

/// Extract image paste IDs from a [QueuedCommand]'s [pastedContents].
///
/// Returns `null` when there are no valid image pastes.
List<int>? getImagePasteIds(Map<int, InputPastedContent>? pastedContents) {
  if (pastedContents == null) return null;
  final ids =
      pastedContents.values
          .where((c) => c.isValidImage)
          .map((c) => c.id)
          .toList();
  return ids.isNotEmpty ? ids : null;
}
